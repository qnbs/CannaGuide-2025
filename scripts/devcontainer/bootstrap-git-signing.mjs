import {
    readFileSync,
    appendFileSync,
    accessSync,
    mkdirSync,
    writeFileSync,
    constants as FS,
} from 'node:fs'
import os from 'node:os'
import path from 'node:path'
import { spawnSync } from 'node:child_process'

const PREFIX = '[devcontainer:bootstrap-git]'

const run = (cmd, args) =>
    spawnSync(cmd, args, {
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'pipe'],
        shell: false,
    })

const gitSet = (key, value) => {
    const result = run('git', ['config', key, value])
    if (result.status !== 0) {
        throw new Error(result.stderr?.trim() || `failed to set ${key}`)
    }
}

const gitUnset = (key) => {
    run('git', ['config', '--local', '--unset', key])
}

/**
 * Neutralise Codespaces' default GIT_COMMITTER_NAME=GitHub / GIT_COMMITTER_EMAIL=noreply@github.com
 * environment variables which override git config user.name/user.email at commit time and cause
 * pre-commit identity checks to fail.
 */
const neutraliseCommitterOverrides = () => {
    const bashrc = path.join(os.homedir(), '.bashrc')
    const marker = '# bootstrap-git-signing: unset committer overrides'
    try {
        if (readFileSync(bashrc, 'utf8').includes(marker)) return
    } catch {
        // File doesn't exist yet — will be created by appendFileSync below
    }
    appendFileSync(
        bashrc,
        `\n${marker}\nunset GIT_COMMITTER_NAME GIT_COMMITTER_EMAIL 2>/dev/null\n`,
    )
    delete process.env.GIT_COMMITTER_NAME
    delete process.env.GIT_COMMITTER_EMAIL
}

/**
 * Detect GitHub Codespaces environment.
 */
const isCodespaces = () => {
    if (process.env.CODESPACES === 'true') return true
    try {
        accessSync('/.codespaces/bin/gh-gpgsign', FS.F_OK)
        return true
    } catch {
        return false
    }
}

/**
 * Check if an SSH signing key exists (generated by user or restored from secret).
 */
const hasSshSigningKey = () => {
    try {
        accessSync(path.join(os.homedir(), '.ssh', 'id_ed25519'), FS.F_OK)
        accessSync(path.join(os.homedir(), '.ssh', 'id_ed25519.pub'), FS.F_OK)
        return true
    } catch {
        return false
    }
}

/**
 * Configure SSH commit signing (preferred -- produces GitHub-verified commits).
 */
const configureSshSigning = () => {
    const pubKey = path.join(os.homedir(), '.ssh', 'id_ed25519.pub')
    gitSet('gpg.format', 'ssh')
    gitSet('user.signingkey', pubKey)
    gitSet('commit.gpgsign', 'true')
    gitSet('tag.gpgsign', 'true')
    console.log(`${PREFIX} SSH signing configured (${pubKey})`)
}

/**
 * Restore SSH signing key from Codespace secret if available.
 */
const restoreSshKeyFromSecret = () => {
    const secret = process.env.SSH_SIGNING_KEY
    if (!secret) return false
    const sshDir = path.join(os.homedir(), '.ssh')
    mkdirSync(sshDir, { recursive: true, mode: 0o700 })
    writeFileSync(path.join(sshDir, 'id_ed25519'), secret + '\n', { mode: 0o600 })
    const result = run('ssh-keygen', ['-y', '-f', path.join(sshDir, 'id_ed25519')])
    if (result.status === 0 && result.stdout.trim()) {
        writeFileSync(path.join(sshDir, 'id_ed25519.pub'), result.stdout.trim() + '\n', {
            mode: 0o644,
        })
        return true
    }
    return false
}

try {
    neutraliseCommitterOverrides()

    if (isCodespaces()) {
        // Priority: SSH signing key (persistent, verified on GitHub).
        // Fallback: Codespaces gh-gpgsign (ephemeral, may show as unverified).
        const restored = restoreSshKeyFromSecret()
        if (restored) {
            console.log(`${PREFIX} Codespaces -- restored SSH key from secret`)
        }
        if (hasSshSigningKey()) {
            configureSshSigning()
            console.log(`${PREFIX} Codespaces -- using SSH signing (verified)`)
        } else {
            gitSet('commit.gpgsign', 'true')
            console.log(`${PREFIX} Codespaces -- using native gh-gpgsign (fallback)`)
            console.log(
                `${PREFIX} [WARN] Commits may show as unverified. Add SSH_SIGNING_KEY Codespace secret for persistent verified signing.`,
            )
        }
    } else {
        // Outside Codespaces: just enable signing (user must configure GPG/SSH themselves)
        gitSet('commit.gpgsign', 'true')
        console.log(`${PREFIX} non-Codespaces environment -- commit signing enabled`)
    }
} catch (error) {
    console.error(`${PREFIX} failed:`, error instanceof Error ? error.message : String(error))
    process.exit(1)
}
