#!/usr/bin/env node
// ---------------------------------------------------------------------------
// generate-service-map.mjs -- Produces a Mermaid dependency diagram for the
// AI service layer.
//
// Usage:  node scripts/generate-service-map.mjs
// Output: docs/architecture/service-dependencies.md
// ---------------------------------------------------------------------------
import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync } from 'node:fs'
import { join, basename, relative } from 'node:path'

const SERVICES_DIR = join(import.meta.dirname, '..', 'apps', 'web', 'services')
const OUTPUT_DIR = join(import.meta.dirname, '..', 'docs', 'architecture')
const OUTPUT_FILE = join(OUTPUT_DIR, 'service-dependencies.md')

// Only scan AI-related service files
const AI_SERVICE_PATTERN =
    /^(ai|gemini|localAi|localAI|LocalAI|AIService|seedbank|growLogRag|inferenceQueue|workerBus|indexedDbLru)/

const NODE_ALIASES = new Map()

/** Convert a filename to a short Mermaid-safe id. */
function toNodeId(filename) {
    const base = basename(filename, '.ts')
    return base.replace(/[^a-zA-Z0-9]/g, '_')
}

/** Extract import source paths from a TypeScript file. */
function extractImports(filePath) {
    const src = readFileSync(filePath, 'utf8')
    const imports = []
    // Static imports
    const staticRe = /from\s+['"](\.[^'"]+)['"]/g
    let m
    while ((m = staticRe.exec(src)) !== null) {
        imports.push(m[1])
    }
    // Dynamic imports
    const dynamicRe = /import\(\s*['"](\.[^'"]+)['"]\s*\)/g
    while ((m = dynamicRe.exec(src)) !== null) {
        imports.push(m[1])
    }
    // Also handle @/ alias imports pointing at services
    const aliasRe = /(?:from|import\()\s*['"]@\/services\/([^'"]+)['"]/g
    while ((m = aliasRe.exec(src)) !== null) {
        imports.push('./' + m[1])
    }
    return imports
}

/** Resolve a relative import to a basename (without extension). */
function resolveImportBase(importPath) {
    const base = basename(importPath).replace(/\.ts$/, '')
    return base
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

const files = readdirSync(SERVICES_DIR)
    .filter((f) => f.endsWith('.ts') && !f.endsWith('.test.ts') && AI_SERVICE_PATTERN.test(f))
    .sort()

// Build adjacency
const edges = []
const nodeSet = new Set()

for (const file of files) {
    const filePath = join(SERVICES_DIR, file)
    const base = basename(file, '.ts')
    const nodeId = toNodeId(file)
    NODE_ALIASES.set(base, nodeId)
    nodeSet.add(nodeId)

    const imports = extractImports(filePath)
    for (const imp of imports) {
        const targetBase = resolveImportBase(imp)
        // Only include edges to files we track
        if (files.some((f) => basename(f, '.ts') === targetBase) && targetBase !== base) {
            const targetId = toNodeId(targetBase + '.ts')
            nodeSet.add(targetId)
            edges.push([nodeId, targetId])
        }
    }
}

// De-duplicate edges
const edgeSet = new Set(edges.map(([a, b]) => `${a} --> ${b}`))

// Build Mermaid
const lines = ['```mermaid', 'flowchart TD']

// Nodes with labels
for (const file of files) {
    const base = basename(file, '.ts')
    const id = toNodeId(file)
    lines.push(`    ${id}["${base}"]`)
}

lines.push('')

// Edges
for (const edge of edgeSet) {
    lines.push(`    ${edge}`)
}

lines.push('```')

// ---------------------------------------------------------------------------
// Write output
// ---------------------------------------------------------------------------

if (!existsSync(OUTPUT_DIR)) {
    mkdirSync(OUTPUT_DIR, { recursive: true })
}

const content = [
    '# AI Service Dependency Map',
    '',
    '> Auto-generated by `scripts/generate-service-map.mjs`.',
    '> Do not edit manually -- re-run the script to update.',
    '',
    ...lines,
    '',
].join('\n')

writeFileSync(OUTPUT_FILE, content, 'utf8')

const nodeCount = nodeSet.size
const edgeCount = edgeSet.size
console.debug(
    `[OK] Service map: ${nodeCount} nodes, ${edgeCount} edges -> ${relative(process.cwd(), OUTPUT_FILE)}`,
)

// ---------------------------------------------------------------------------
// Acyclic enforcement (K-03) -- DFS-based cycle detection
// ---------------------------------------------------------------------------

function detectCycles(nodeSet, edges) {
    const adj = new Map()
    for (const id of nodeSet) adj.set(id, [])
    for (const [a, b] of edges) {
        if (adj.has(a)) adj.get(a).push(b)
    }

    const WHITE = 0,
        GRAY = 1,
        BLACK = 2
    const color = new Map()
    for (const id of nodeSet) color.set(id, WHITE)
    const cycles = []

    function dfs(node, path) {
        color.set(node, GRAY)
        path.push(node)
        for (const neighbor of adj.get(node) ?? []) {
            if (color.get(neighbor) === GRAY) {
                const cycleStart = path.indexOf(neighbor)
                cycles.push([...path.slice(cycleStart), neighbor])
            } else if (color.get(neighbor) === WHITE) {
                dfs(neighbor, path)
            }
        }
        path.pop()
        color.set(node, BLACK)
    }

    for (const id of nodeSet) {
        if (color.get(id) === WHITE) dfs(id, [])
    }
    return cycles
}

const cycles = detectCycles(nodeSet, edges)

// Known cycles that are accepted (lazy-loaded, no runtime issue)
const KNOWN_CYCLES = new Set([
    'LocalAIInfrastructure -> localAiPreloadService -> localAI -> localAiInfrastructureService -> LocalAIInfrastructure',
    'localAiPreloadService -> localAI -> localAiInfrastructureService -> localAiPreloadService',
])

const unknownCycles = cycles.filter((c) => !KNOWN_CYCLES.has(c.join(' -> ')))

if (cycles.length > 0) {
    const knownCount = cycles.length - unknownCycles.length
    if (knownCount > 0) {
        console.debug(
            `[INFO] ${knownCount} known circular dependency cycle(s) (lazy-loaded, accepted).`,
        )
    }
}

const strict = process.argv.includes('--strict')
if (unknownCycles.length > 0) {
    console.error(`[FAIL] ${unknownCycles.length} NEW circular dependency cycle(s) detected:`)
    for (const cycle of unknownCycles) {
        console.error(`  ${cycle.join(' -> ')}`)
    }
    process.exit(1)
} else if (strict && cycles.length > 0) {
    console.error(`[FAIL] --strict: ${cycles.length} cycle(s) exist (including known).`)
    process.exit(1)
} else {
    console.debug('[OK] No new circular dependencies detected.')
}
