/**
 * generate-strain-files.mjs
 *
 * Reads apps/web/data/strains/strains.json (Single Source of Truth)
 * and generates uniform TypeScript strain data files (a.ts-z.ts, numeric.ts)
 * plus index.ts.
 *
 * Usage: node scripts/generate-strain-files.mjs
 */

import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const strainsDir = path.join(repoRoot, 'apps', 'web', 'data', 'strains')
const jsonPath = path.join(strainsDir, 'strains.json')

// -----------------------------------------------------------------------
// Helpers
// -----------------------------------------------------------------------

const HEADER = `// AUTO-GENERATED by scripts/generate-strain-files.mjs
// Do not edit manually. Edit strains.json and run: pnpm run strains:generate
import { type Strain, StrainType } from '@/types'
import { createStrainObject } from '@/services/strainFactory'`

/**
 * Format a strain object as a createStrainObject({...}) call.
 * Uses consistent single-quote TS style matching project conventions.
 */
const formatStrain = (strain) => {
    const lines = []
    lines.push('    createStrainObject({')

    /** Escape a string for embedding in a single-quoted JS literal. */
    const escapeStr = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'")

    const addField = (key, value) => {
        if (value === undefined || value === null) return

        if (typeof value === 'string') {
            const escaped = escapeStr(value)
            lines.push(`        ${key}: '${escaped}',`)
        } else if (typeof value === 'number') {
            lines.push(`        ${key}: ${value},`)
        } else if (Array.isArray(value)) {
            const items = value.map((v) => {
                const escaped = escapeStr(v)
                return `'${escaped}'`
            })
            if (items.join(', ').length < 80) {
                lines.push(`        ${key}: [${items.join(', ')}],`)
            } else {
                lines.push(`        ${key}: [`)
                for (const item of items) {
                    lines.push(`            ${item},`)
                }
                lines.push(`        ],`)
            }
        } else if (typeof value === 'object') {
            lines.push(`        ${key}: ${formatObject(value, 8)},`)
        }
    }

    // Type gets StrainType enum
    const addType = (strain) => {
        const typeMap = {
            Sativa: 'StrainType.Sativa',
            Indica: 'StrainType.Indica',
            Hybrid: 'StrainType.Hybrid',
        }
        lines.push(`        type: ${typeMap[strain.type] ?? 'StrainType.Hybrid'},`)
    }

    // Ordered fields for consistency
    addField('id', strain.id)
    addField('name', strain.name)
    addType(strain)
    if (strain.typeDetails) addField('typeDetails', strain.typeDetails)
    if (strain.genetics) addField('genetics', strain.genetics)
    addField('floweringType', strain.floweringType)
    if (strain.thc !== undefined) addField('thc', strain.thc)
    if (strain.cbd !== undefined) addField('cbd', strain.cbd)
    if (strain.cbg !== undefined) addField('cbg', strain.cbg)
    if (strain.thcv !== undefined) addField('thcv', strain.thcv)
    if (strain.thcRange) addField('thcRange', strain.thcRange)
    if (strain.cbdRange) addField('cbdRange', strain.cbdRange)
    if (strain.floweringTime !== undefined) addField('floweringTime', strain.floweringTime)
    if (strain.floweringTimeRange) addField('floweringTimeRange', strain.floweringTimeRange)
    if (strain.description) addField('description', strain.description)
    if (strain.agronomic) addField('agronomic', strain.agronomic)
    if (strain.aromas) addField('aromas', strain.aromas)
    if (strain.dominantTerpenes) addField('dominantTerpenes', strain.dominantTerpenes)
    if (strain.lineage) addField('lineage', strain.lineage)

    lines.push('    }),')
    return lines.join('\n')
}

/**
 * Format a nested plain object with proper indentation.
 */
const formatObject = (obj, indent) => {
    const pad = ' '.repeat(indent)
    const innerPad = ' '.repeat(indent + 4)
    const entries = Object.entries(obj)

    // Check if all values are simple (strings/numbers) for inline format
    const allSimple = entries.every(([, v]) => typeof v === 'string' || typeof v === 'number')

    /** Escape a string for embedding in a single-quoted JS literal. */
    const esc = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'")

    if (allSimple && entries.length <= 3) {
        const parts = entries.map(([k, v]) => {
            if (typeof v === 'string') {
                return `${k}: '${esc(v)}'`
            }
            return `${k}: ${v}`
        })
        return `{ ${parts.join(', ')} }`
    }

    const lines = ['{']
    for (const [key, value] of entries) {
        if (typeof value === 'string') {
            lines.push(`${innerPad}${key}: '${esc(value)}',`)
        } else if (typeof value === 'number') {
            lines.push(`${innerPad}${key}: ${value},`)
        } else if (typeof value === 'object' && value !== null) {
            lines.push(`${innerPad}${key}: ${formatObject(value, indent + 4)},`)
        }
    }
    lines.push(`${pad}}`)
    return lines.join('\n')
}

/**
 * Get the letter bucket for a strain name.
 */
const getBucket = (name) => {
    const first = name.charAt(0).toLowerCase()
    if (/[0-9]/.test(first)) return 'numeric'
    if (/[a-z]/.test(first)) return first
    return 'numeric' // fallback
}

const EXPORT_NAMES = {
    numeric: 'strainsNumeric',
    a: 'strainsA',
    b: 'strainsB',
    c: 'strainsC',
    d: 'strainsD',
    e: 'strainsE',
    f: 'strainsF',
    g: 'strainsG',
    h: 'strainsH',
    i: 'strainsI',
    j: 'strainsJ',
    k: 'strainsK',
    l: 'strainsL',
    m: 'strainsM',
    n: 'strainsN',
    o: 'strainsO',
    p: 'strainsP',
    q: 'strainsQ',
    r: 'strainsR',
    s: 'strainsS',
    t: 'strainsT',
    u: 'strainsU',
    v: 'strainsV',
    w: 'strainsW',
    x: 'strainsX',
    y: 'strainsY',
    z: 'strainsZ',
}

// -----------------------------------------------------------------------
// Main
// -----------------------------------------------------------------------

const run = async () => {
    const raw = await fs.readFile(jsonPath, 'utf8')
    const strains = JSON.parse(raw)
    console.log(`[generate] Loaded ${strains.length} strains from strains.json`)

    // Bucket strains by first letter
    const buckets = new Map()
    for (const strain of strains) {
        const bucket = getBucket(strain.name)
        if (!buckets.has(bucket)) buckets.set(bucket, [])
        buckets.get(bucket).push(strain)
    }

    // Sort within each bucket by name
    for (const [, list] of buckets) {
        list.sort((a, b) => a.name.localeCompare(b.name))
    }

    // Generate strain files
    let totalWritten = 0
    const bucketKeys = [...buckets.keys()].sort((a, b) => {
        if (a === 'numeric') return -1
        if (b === 'numeric') return 1
        return a.localeCompare(b)
    })

    for (const key of bucketKeys) {
        const list = buckets.get(key)
        const exportName = EXPORT_NAMES[key]
        if (!exportName) {
            console.warn(`[generate] Unknown bucket: ${key}`)
            continue
        }

        const strainBlocks = list.map(formatStrain)
        const content = [
            HEADER,
            '',
            `export const ${exportName}: Strain[] = [`,
            strainBlocks.join('\n'),
            ']',
            '',
        ].join('\n')

        const filePath = path.join(strainsDir, `${key}.ts`)
        await fs.writeFile(filePath, content, 'utf8')
        totalWritten += list.length
        console.log(`[generate]   ${key}.ts: ${list.length} strains`)
    }

    // Generate index.ts
    const indexContent = generateIndex(bucketKeys)
    await fs.writeFile(path.join(strainsDir, 'index.ts'), indexContent, 'utf8')
    console.log(`[generate]   index.ts: generated`)

    console.log(`[generate] ----------------------------------------`)
    console.log(`[generate] Total written: ${totalWritten} strains in ${bucketKeys.length} files`)
}

const generateIndex = (bucketKeys) => {
    const lines = [
        '// AUTO-GENERATED by scripts/generate-strain-files.mjs',
        '// Do not edit manually. Edit strains.json and run: pnpm run strains:generate',
        "import type { Strain } from '@/types'",
        '',
    ]

    // Imports
    for (const key of bucketKeys) {
        const exportName = EXPORT_NAMES[key]
        lines.push(`import { ${exportName} } from './${key}'`)
    }

    lines.push('')
    lines.push('/**')
    lines.push(' * Combined strain catalog.')
    lines.push(' * All strains are organized in alphabetical files (a.ts-z.ts, numeric.ts).')
    lines.push(' * De-duplicated by ID to ensure data integrity.')
    lines.push(' */')
    lines.push('const combinedStrains: Strain[] = [')
    for (const key of bucketKeys) {
        const exportName = EXPORT_NAMES[key]
        lines.push(`    ...${exportName},`)
    }
    lines.push(']')
    lines.push('')
    lines.push('// De-duplicate strains by ID to ensure data integrity from the source.')
    lines.push(
        'const deduplicatedMap = new Map(combinedStrains.map((strain) => [strain.id, strain]))',
    )
    lines.push('')
    lines.push('if (import.meta.env.DEV && deduplicatedMap.size < combinedStrains.length) {')
    lines.push('    const duplicateCount = combinedStrains.length - deduplicatedMap.size')
    lines.push('    const idCounts = new Map<string, number>()')
    lines.push('    for (const strain of combinedStrains) {')
    lines.push('        idCounts.set(strain.id, (idCounts.get(strain.id) ?? 0) + 1)')
    lines.push('    }')
    lines.push(
        '    const duplicateIds = [...idCounts.entries()].filter(([, count]) => count > 1).map(([id]) => id)',
    )
    lines.push('    console.debug(')
    lines.push(
        "        `[Strains] ${duplicateCount} duplicate strain(s) detected and auto-deduplicated. IDs: ${duplicateIds.join(', ')}`,",
    )
    lines.push('    )')
    lines.push('}')
    lines.push('')
    lines.push('export const allStrainsData: Strain[] = Array.from(deduplicatedMap.values())')
    lines.push('')

    return lines.join('\n')
}

run().catch((err) => {
    console.error('[generate] Fatal error:', err)
    process.exit(1)
})
